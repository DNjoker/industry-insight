def _read_report_sections(report_path: str, max_chars: int = 6000) -> str:
    """Read key sections from an industry report for selling point context.

    Extracts: 消费者行为/痛点, 竞品分析, 经营打法/爆品趋势 sections.
    """
    if not report_path:
        return ""

    # Try relative to Obsidian vault first, then as absolute path
    vault = get_vault_path()
    candidates = []
    if vault:
        candidates.append(Path(vault) / report_path)
    candidates.append(Path(report_path))

    content = None
    for p in candidates:
        try:
            if p.exists():
                content = p.read_text(encoding="utf-8")
                break
        except Exception:
            continue

    if not content:
        logger.warning(f"Report not found: {report_path}")
        return ""

    # Extract key sections by heading markers
    sections: list[str] = []
    capture = False
    section_buf: list[str] = []
    # Section headings that contain relevant context for selling points
    trigger_headings = [
        "消费者", "用户", "痛点", "需求", "行为", "画像",
        "竞争格局", "主要玩家", "竞品", "对手",
        "经营打法", "爆品", "趋势", "素材", "文案", "卖点",
        "定价", "价格", "渠道", "投放",
    ]

    for line in content.splitlines():
        if line.startswith("## "):
            # Save previous section if capturing
            if capture and section_buf:
                text = "\n".join(section_buf)
                if len(text) > 100:
                    sections.append(text)
                section_buf = []

            heading = line[3:].strip()
            capture = any(kw in heading for kw in trigger_headings)
            if capture:
                section_buf = [line]
        elif capture:
            section_buf.append(line)

    # Don't miss the last section
    if capture and section_buf:
        text = "\n".join(section_buf)
        if len(text) > 100:
            sections.append(text)

    if not sections:
        # Fallback: read first 4000 chars of the report
        return content[:max_chars]

    combined = "\n\n---\n\n".join(sections)
    if len(combined) > max_chars:
        combined = combined[:max_chars] + "\n\n...（报告内容已截断，完整报告见 Obsidian）"
    return combined


