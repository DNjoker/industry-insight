@router.post("/selling-point")
async def generate_selling_point(request: SellingPointRequest):
    """Generate platform-specific main image copy and detail page structure."""
    config_error = _validate_config()
    if config_error:
        return {"error": config_error}

    # Select prompt: custom > template > default
    # For content-only requests, use a focused prompt that skips e-commerce sections
    is_content_only = (not request.platforms) and bool(request.content_platforms)

    if request.custom_system_prompt.strip():
        system_prompt = request.custom_system_prompt.strip()
    elif is_content_only and len(request.content_platforms) == 1:
        platform_prompt = _get_content_prompt(request.content_platforms[0])
        system_prompt = platform_prompt if platform_prompt else CONTENT_ONLY_PROMPT
    elif is_content_only:
        system_prompt = CONTENT_ONLY_PROMPT
    elif request.template and request.template != "通用":
        system_prompt = _apply_template(request.template)
    else:
        system_prompt = SELLING_POINT_SYSTEM_PROMPT

    if request.regenerate_section and request.existing_result:
        # Targeted regeneration: rebuild only one section
        result_data = await _regenerate_section(
            system_prompt=system_prompt,
            user_prompt=_build_user_prompt(request),
            section=request.regenerate_section,
            existing=request.existing_result,
        )
        return result_data

    if request.refine_issues and request.existing_result:
        # Refinement from self-review: fix identified issues
        import copy
        all_ecom_p = {"淘宝", "拼多多", "抖音", "京东", "快手"}
        all_content_p = {"小红书", "公众号", "抖音内容", "快手", "视频号", "B站"}

        # Prune existing_result so AI doesn't see (and regenerate) unselected platforms
        pruned_existing = copy.deepcopy(request.existing_result)
        for pkey in list(pruned_existing.get("platforms", {}).keys()):
            if pkey in all_ecom_p and pkey not in set(request.platforms):
                del pruned_existing["platforms"][pkey]
        for ckey in list(pruned_existing.get("content_assets", {}).keys()):
            if ckey in all_content_p and ckey not in set(request.content_platforms):
                del pruned_existing["content_assets"][ckey]

        # For content results, the review is stored in content_reviews[platform], not quality_review
        review = pruned_existing.get("quality_review", {})
        if not review and is_content_only and request.content_platforms:
            content_reviews = pruned_existing.get("content_reviews", {})
            platform_review = content_reviews.get(request.content_platforms[0], {})
            if platform_review:
                review = platform_review
        if not review:
            review = {"issues": request.refine_issues, "summary": "用户指定问题"}

        # Pass platform lists so _refine_from_review can prune before self-review
        result_data = await _refine_from_review(
            result_data=pruned_existing,
            review=review,
            product_name=request.product_name,
            category=request.category,
            system_prompt=system_prompt,
            is_content=is_content_only,
            platforms=list(request.platforms),
            content_platforms=list(request.content_platforms),
            target_audience=request.target_audience,
            price_tier=request.price_tier,
        )
        return result_data

    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": _build_user_prompt(request)},
    ]

    logger.info(
        f"Generate: platforms={request.platforms}, content_platforms={request.content_platforms}, "
        f"is_content_only={is_content_only}, prompt_kind={'platform:' + request.content_platforms[0] if is_content_only and len(request.content_platforms) == 1 else 'default'}"
    )

    full_response = ""
    async for chunk in chat_stream(messages, temperature=0.7, max_tokens=24576):
        full_response += chunk
    usage_gen = get_last_usage()
    reset_usage()

    cleaned = _extract_json(full_response.strip())
    cleaned = _repair_json(cleaned)

    try:
        data = json.loads(cleaned)
    except json.JSONDecodeError:
        logger.warning(
            f"Failed to parse selling point JSON, raw length: {len(full_response)}, "
            f"head: {full_response[:200]}, tail: {full_response[-200:]}"
        )
        return {"raw": full_response, "error": "JSON解析失败，请重试"}

    # Normalize content_assets keys for single-platform requests
    if is_content_only and len(request.content_platforms) == 1:
        expected_key = request.content_platforms[0]
        ca = data.get("content_assets", {})
        if isinstance(ca, dict) and expected_key not in ca:
            # AI may have used a wrong key — remap to expected platform name
            remapped = {}
            for k, v in ca.items():
                if k == expected_key:
                    remapped[k] = v
                elif k in ("social_post", "video_script"):
                    # Flat format — nest under expected platform name
                    remapped[expected_key] = {k: v for k, v in ca.items()}
                    break
                elif isinstance(v, dict) and (v.get("social_post") or v.get("video_script")):
                    # Wrong platform name used as key — fix it
                    remapped[expected_key] = v
                else:
                    remapped[k] = v
            if not remapped:
                remapped[expected_key] = {}
            data["content_assets"] = remapped
            logger.warning(
                f"Fixed content_assets keys: expected '{expected_key}', "
                f"got {list(ca.keys()) if ca else 'none'}"
            )

    # ── Prune unselected platforms ────────────────────────────
    # AI sometimes generates all platforms despite instructions; strip extras
    all_ecom_p = {"淘宝", "拼多多", "抖音", "京东", "快手"}
    all_content_p = {"小红书", "公众号", "抖音内容", "快手", "视频号", "B站"}

    platforms = data.get("platforms", {})
    if isinstance(platforms, dict):
        selected = set(request.platforms)
        for key in list(platforms.keys()):
            if key in all_ecom_p and key not in selected:
                del platforms[key]
                logger.info(f"Pruned unselected e-commerce platform: {key}")

    ca = data.get("content_assets", {})
    if isinstance(ca, dict):
        selected = set(request.content_platforms)
        for key in list(ca.keys()):
            if key in all_content_p and key not in selected:
                del ca[key]
                logger.info(f"Pruned unselected content platform: {key}")

    # ── Ensure design_rationale (DeepSeek sometimes skips it) ──
    if not is_content_only:
        _ensure_design_rationale(data, request.target_audience, request.price_tier)

    # ── Self-review ───────────────────────────────────────────
    try:
        if is_content_only:
            review = await _self_review_content(data, request.product_name, request.category, list(request.content_platforms))
        else:
            review = await _self_review(data, request.product_name, request.category)
        data["quality_review"] = review
    except Exception as e:
        logger.warning(f"Self-review failed to attach: {e}")
    usage_review = get_last_usage()

    # Accumulate token usage
    data["token_usage"] = {
        "prompt_tokens": usage_gen.get("prompt_tokens", 0) + usage_review.get("prompt_tokens", 0),
        "completion_tokens": usage_gen.get("completion_tokens", 0) + usage_review.get("completion_tokens", 0),
        "total_tokens": usage_gen.get("total_tokens", 0) + usage_review.get("total_tokens", 0),
    }

    return data


