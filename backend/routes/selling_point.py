async def get_template_prompt(request: dict):
    """Return the full system prompt for a given template key."""
    template_key = request.get("key", "通用")
    prompt = _apply_template(template_key)
    return {"key": template_key, "system_prompt": prompt}


@router.post("/selling-point/save")
async def save_selling_point_result(request: SaveSellingPointRequest):
    """Save generated selling point results to Obsidian vault."""
    try:
        result = save_selling_point(
            product_name=request.product_name,
            data=request.data,
            platforms=request.platforms,
            category=request.category,
            save_raw=request.save_raw,
        )
        return result
    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))
    except Exception as e:
        logger.error(f"Save selling point failed: {e}")
        raise HTTPException(status_code=500, detail=str(e))


async def _regenerate_section(
    system_prompt: str,
    user_prompt: str,
    section: str,
    existing: dict,
) -> dict:
    """Regenerate a single section of the selling point result, keeping the rest."""
    parts = section.split(".", 1)
    platform = parts[0] if len(parts) >= 1 else ""
    field = parts[1] if len(parts) >= 2 else ""

    # Map field name to human-readable description
    field_names = {
        "main_images": "主图文案",
        "selling_points": "FAB卖点拆解",
        "detail_page": "详情页排版与文案",
        "design_brief": "主图设计简报",
        "additional_images": "附图设计",
    }
    field_desc = field_names.get(field, field)

    regen_prompt = (
        f"{system_prompt}\n\n"
        f"## 局部重生任务\n"
        f"用户对「{platform}」平台的「{field_desc}」部分不满意，需要你重新生成这个部分。\n\n"
        f"### 规则\n"
        f"1. 只重新输出整个 JSON，其他平台和其他字段必须和原结果完全一致\n"
        f"2. 仅修改 {platform}.{field} 的内容\n"
        f"3. 保持和原结果相同的 JSON 结构\n\n"
        f"### 原结果（JSON）\n"
        f"```json\n{json.dumps(existing, ensure_ascii=False, indent=2)}\n```\n\n"
        f"请输出完整的修改后 JSON。"
    )

    messages = [
        {"role": "system", "content": regen_prompt},
        {"role": "user", "content": user_prompt},
    ]

    full_response = ""
    async for chunk in chat_stream(messages, temperature=0.8, max_tokens=8192):
        full_response += chunk

    cleaned = _extract_json(full_response.strip())
    cleaned = _repair_json(cleaned)

    try:
        new_data = json.loads(cleaned)
    except json.JSONDecodeError:
        logger.warning(f"Regenerate JSON parse failed for section {section}")
        return existing  # fallback to original

    # Preserve content_assets from the original — section regeneration only touches e-commerce data
    if existing.get("content_assets"):
        new_data["content_assets"] = existing["content_assets"]
    if existing.get("content_reviews"):
        new_data["content_reviews"] = existing["content_reviews"]

    return new_data


