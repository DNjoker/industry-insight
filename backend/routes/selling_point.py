def _extract_json(text: str) -> str:
    """Extract JSON from LLM response, handling markdown wrapping and truncation."""
    import re

    # Try to find JSON in ```json...``` blocks
    code_blocks = re.findall(r"```(?:json)?\s*\n?(.*?)```", text, re.DOTALL)
    if code_blocks:
        # Use the longest code block
        return max(code_blocks, key=len).strip()

    # No code blocks: find outermost { ... }
    start = text.find("{")
    end = text.rfind("}")
    if start != -1 and end > start:
        return text[start:end + 1]

    return text


def _repair_json(text: str) -> str:
    """Attempt to repair common JSON issues from LLM output."""
    import re

    # Remove trailing commas before ] or }
    text = re.sub(r",\s*(\]|\})", r"\1", text)
    # Remove comments (// or # style lines)
    text = re.sub(r"^\s*(?://|#).*$", "", text, flags=re.MULTILINE)
    # Fix single quotes
    # (skip for now - too risky with apostrophes in Chinese text)

    return text


