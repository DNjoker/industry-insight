def _apply_template(template_key: str) -> str:
    """Apply industry template overrides to the base system prompt."""
    prompt = SELLING_POINT_SYSTEM_PROMPT
    template = INDUSTRY_TEMPLATES.get(template_key)
    if not template:
        return prompt

    sections = template.get("sections", {})
    if not sections:
        return prompt

    # Replace the "detail examples" section (from "## 详情页核心原则" to "**AIDA")
    if "detail_examples" in sections:
        prompt = _replace_prompt_section(
            prompt,
            "## 详情页核心原则：一屏一卖点",
            "**AIDA 在详情页中的落地",
            sections["detail_examples"],
        )

    # Replace FAB model section
    if "fab_note" in sections:
        prompt = _replace_prompt_section(
            prompt,
            "**FAB 卖点提炼模型：**",
            "**AIDA 在详情页中的落地",
            sections["fab_note"],
        )

    # Replace detail page screen count (default "6-8屏" → template-specific)
    if "detail_screens" in sections:
        prompt = prompt.replace("6-8屏", sections["detail_screens"])

    return prompt


def _replace_prompt_section(prompt: str, start_marker: str, stop_marker: str, replacement: str) -> str:
    """Replace the section between start_marker and stop_marker with replacement."""
    start_idx = prompt.find(start_marker)
    stop_idx = prompt.find(stop_marker, start_idx) if start_idx != -1 else -1
    if start_idx != -1 and stop_idx != -1:
        return prompt[:start_idx] + replacement + "\n\n" + prompt[stop_idx:]
    return prompt


